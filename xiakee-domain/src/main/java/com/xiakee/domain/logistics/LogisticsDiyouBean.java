package com.xiakee.domain.logistics;

public class LogisticsDiyouBean {
	private String orderid;
	private Long infoId;
	private int node;
	private UdfexDetailBean udfexDetailBean;

	public LogisticsDiyouBean() {

	}

	// 短信发送功能开关
	private boolean openSmsc = false;

	public String getOrderid() {
		return orderid;
	}

	public void setOrderid(String orderid) {
		this.orderid = orderid;
	}

	public int getNode() {
		return node;
	}

	public void setNode(int node) {
		this.node = node;
	}

	public Long getInfoId() {
		return infoId;
	}

	public void setInfoId(Long infoId) {
		this.infoId = infoId;
	}

	public UdfexDetailBean getUdfexDetailBean() {
		return udfexDetailBean;
	}

	public void setUdfexDetailBean(UdfexDetailBean udfexDetailBean) {
		this.udfexDetailBean = udfexDetailBean;
	}

	public boolean isOpenSmsc() {
		return openSmsc;
	}

	public void setOpenSmsc(boolean openSmsc) {
		this.openSmsc = openSmsc;
	}

	@Override
	public String toString() {
		return "物流节点信息： [orderid=" + orderid + ", infoId=" + infoId + ", udfexDetailBean=" + udfexDetailBean + ", openSmsc=" + openSmsc + "]";
	}
}
